package zoom.city.android.main.helper;

public class Notification {
	public String title;
	public String text;
	public String image;
	public String link;
	
	public Notification(String title, String text, String image, String link) {
		super();
		this.title = title;
		this.text = text;
		this.image = image;
		this.link = link;
	}
	
}
