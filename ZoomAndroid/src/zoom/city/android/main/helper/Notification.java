package zoom.city.android.main.helper;

public class Notification {
	private String title;
	private String text;
	private String image;
	private String link;
	
	public Notification(String title, String text, String image, String link) {
		super();
		this.title = title;
		this.text = text;
		this.image = image;
		this.link = link;
	}
	
}
